package abteilungen;

public class Abteilung {
	
	private int id;
	private int nameAbteilung;

	public void setId(int id) {
		this.id = id;
	}

	public int getId() {
		return id;
	}

	public void setNameAbteilung(int nameAbteilung) {
		this.nameAbteilung = nameAbteilung;
	}

	public int getNameAbteilung() {
		return nameAbteilung;
	}

}
